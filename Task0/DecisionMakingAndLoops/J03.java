//Java program to display alphabets( A to Z) using loop.

package Task0.DecisionMakingAndLoops;

public class J03 {
    public static void main(String[] args) {

        System.out.print(" Alphabets (A to Z) : ");

        for(int i = 65; i<=90; i++) {
            char ch = (char) i;
            System.out.print( ch + " ");
        }
    }

}
